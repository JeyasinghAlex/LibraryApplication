public class Book {
    String bookName;
    String authorName;
    String publisherName;
    String year;

    public Book(String Bname , String Aname , String Pname , String yr ){
        this.bookName = Bname;
        this.authorName = Aname;
        this.publisherName = Pname;
        this.year = yr;
    }

}
